#include "bite.h"

Tile Bite::bite = { ' ', Color::white, "bite", true };

Bite::Bite() :
	Weapon( bite, -1, -1, 1.5, nullptr )
{
}

int Bite::damage()
{
	return rand() % 4 + 2;	// 1d4 + 1
}
